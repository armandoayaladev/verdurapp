    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        if (!this.txt_consecutivo.getText().equals("")) {
            Configuraciones config = new Configuraciones();
            config.configurarConsecutivo(Integer.parseInt(this.txt_consecutivo.getText()));
            this.dispose();
            JOptionPane.showMessageDialog(null,"Se ha configurado el consecitivo exitosamente");
        } else {
            JOptionPane.showMessageDialog(null,"Se ha producido un error al configurar el consecutivo");
        }
    }//GEN-LAST:event_btn_guardarActionPerformed


